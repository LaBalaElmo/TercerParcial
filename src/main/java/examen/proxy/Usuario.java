package examen.proxy;

public class Usuario {
	private String pass;
	private String user;
	public Usuario(String pass, String user) {
		super();
		this.pass = pass;
		this.user = user;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	
	

}
